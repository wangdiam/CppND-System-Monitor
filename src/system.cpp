Processor& System::Cpu() { return _cpu; }

vector<Process>& System::Processes() { 
    _processes = {};
    auto pids = LinuxParser::Pids();
    for (auto pid : pids) {
        Process P(pid);
        _processes.push_back(P);
    }
    std::sort(_processes.begin(),_processes.end());
    return _processes; 
}

